#include <bits/stdc++.h>

using namespace std;

int main(){
	cout.precision(1);
	cout << fixed;
	int a,b;
	string c;
	cin >> a;
	cin >> b;
	cin >> c;
	if(c=="+"){
		cout << a + b << endl;
	}
	else if(c=="-"){
		cout << a - b  << endl;
	}else if(c=="*"){
		cout << a * b <<   endl;
	}else if(c=="/"){
		cout << a / b << ".0" << endl;
	}else if(c=="%"){
		cout << a % b << endl;
	}else if(c=="^"){
		long long a;
		long long b;
		cout << (a ^ b) << endl;
	}
}
